export function getMonthDiff(start, current) {
    if (!start || !current) return 0;
    const d1 = new Date(start + "-01");
    const d2 = new Date(current + "-01");
    return (d2.getFullYear() - d1.getFullYear()) * 12 + (d2.getMonth() - d1.getMonth());
}

